		//int (*funct_ptr)() - указатель на ф-ию которая будет вызвана, если событие произойдет
		//param - ?

/*
**Событие: мыши
*/
int		mlx_mouse_hook(void *win_ptr, int (*funct_ptr)(), void *param);
		//win_ptr - идентификатор окна
		//int (*funct_ptr)() - указатель на ф-ию которая будет вызвана, если событие произойдет
