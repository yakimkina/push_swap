//?ОБЩИЕ ОПРРЕДЕЛЕНИЯ
//!Разрешающая способность - характеризует расстояние между соседними пикселами. Измеряют кол-вом пикселов на еденицу длины(Грубо говоря это расстояние с которого уже не будут видны отдельные пикселы)
//!Растр - матрица ячеек(пикселов)
//!dpi(dots per inch) - количество пикселов в одном дюйме длины(2.54см)

//?КЛАСИФИКАЦИЯ ЦВЕТОВ ИЗОБРАЖЕНИЯ
//!Двухцветные(бинарные) изображения - на 1 пиксел 1 бит(черно-белые изображения в основном)
//!Полутоновые изображения - градации серого или иного цвета. 256 градаций(1 байт на пиксел)
//!Цветные изображения - от 2-х бит и выше на пиксел. Глубина цвета 16/32/48 или более на пиксел

//?ХАРАКТЕРИСТИКИ ЦВЕТА
//!Цветовой тон - позволяет отличать один цвет от другого
//!Яркость - выражает кол-во воспринимаемого света
//!Насыщенность - выражается долей присутствия белого цвета


//?MLX.1
/*
**Создает связь нашей программы с графикой macOS
**Возвращает логин подключения(идентификатор соединения) к графическому серверу mac
*/
void	*mlx_init();

//?MLX_NEW_WINDOW.1
/*
**Создает новое окно
**Возвращает идентификатор окна. Если мы хотим нарисовать что-то в окне, нам нужен его идентификатор
**Защита: может вернуть NULL если не удалось установить соединение
*/
void	*mlx_new_window(void *mlx_ptr, int size_x, int size_y, char *title);
		//mlx_ptr - идентификатор соединения
		//size_x - ширина окна
		//size_y - высота окна
		//title - заголовок окна

/*
**Очищает черным цветов окно/удаляет окно
**Возвращает невалидное значение(оно ничего не значит)
*/
int		mlx_clear_window(void *mlx_ptr, void *win_ptr);
int		mlx_destroy_window(void *mlx_ptr, void *win_ptr);
		//mlx_ptr - идентификатор соединения
		//win_ptr - идентификатор окна

//?MLX_PIXEL_PUT
/*
**Рисует точку на окне
**начало отсчета верхний левый угол
*/
int		mlx_pixel_put(void *mlx_ptr, void *win_ptr, int x, int y, int color);
		//mlx_ptr - идентификатор соединения
		//win_ptr - идентификатор окна
		//x - расположение точки относительно оси х
		//y - расположение точки относительно оси y
		//color - цвет точки

/*
**Вставляет строку в окно которая передается параметром string
**Возвращает невалидное значение
*/
int		mlx_string_put(void *mlx_ptr, void *win_ptr, int x, int y, int color, char *string);
		//mlx_ptr - идентификатор соединения
		//win_ptr - иденнтификатор окна
		//x - расположение строки относительно оси х
		//y - расположение строки относительно оси у
		//color - цвет строки
		//string - строка

//?MLX_NEW_IMAGE
/*
**Выделяет память для будующего изображения
**Возвращает идендификатор этого изображения
**Защита: Если возвращает NULL значит не удалось выделить память для изображения
*/
void	*mlx_new_image ( void *mlx_ptr, int width, int height );
		//mlx_ptr - идендификатор соединения
		//width - ширина изображения
		//height - высота изображения

/*
**Возвращает возвращает начало области памяти где расположенно изображение
*/
char 	*mlx_get_data_addr ( void *img_ptr, int *bits_per_pixel, int *size_line, int *endian );
		//img_ptr - Определяет используемое изображение(то изображение с которым ф-ия будет работать)
		//bits_per_pixel - число битов необходимых для представления одно пикселя
		//size_line - количество байт, используемых для хранения одной строки изображения в памяти
		//endian - система расположения чисел(macOS little_endian)

/*
**Уничтожает изображение
*/
int		mlx_destroy_image(void *mlx_ptr, void *img_ptr);
		//mlx_ptr - идентификатор соединения
		//img_ptr - идентификатор изображение(оно будет удалено)

/*
**Возвращает цвет для текущей системы(Но это не точно)
*/
unsigned int mlx_get_color_value (void *mlx_ptr, int color);
		//mlx_ptr - идентификатор соединения
		//color - цвет в RGB

/*
**Вставляет изображение в окно
*/
int		mlx_put_image_to_window ( void *mlx_ptr, void *win_ptr, void *img_ptr, int x, int y );
		//mlx_ptr - идентификатор соединения
		//win_ptr - идентификатор окна
		//img_ptr - идентификатор изображения
		//x - координата расположения изображения по оси х
		//y - координата расположения у

/*
**Создает изображение
**Защита: возвращает NULL, если не удалось создать
*/
void 	*mlx_xpm_to_image(void *mlx_ptr, char **xpm_data, int *width, int *height);
		//mlx_ptr - идентификатор соединения
		//xpm_data - ?
		//width - ширина
		//height - высота

/*
**Создает изображение
**Защита: возвращает NULL, если не удалось создать
*/
void	*mlx_xpm_file_to_image(void *mlx_ptr, char *filename, int *width, int *height);
		//mlx_ptr - идентификатор соединения
		//filename - имя файла
		//width - ширина
		//height - высота


//?MLX_LOOP
/*
**Обработчик событий
**Управляет событиями на окне и передачей их в нашу программу
**Она работает в бесконечном цикле пока не произойдет какое-то событие
*/
int		mlx_loop(void *mlx_ptr);
		//mlx_ptr - идентификатор соединения

/*
**Событие: Клавиатура
*/
int		mlx_key_hook(void *win_ptr, int (*funct_ptr)(), void *param);
